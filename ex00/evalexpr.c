#include "evalexpr.h"

int		eval_expr(char *str)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while ((str[i] >= '0' && str[i] <= '9') && str[i])
		{
			ft_putchar(ft_to_int(&str[i]));
			i++;
		}
	ft_putchar('\n');
	return (0);
}
